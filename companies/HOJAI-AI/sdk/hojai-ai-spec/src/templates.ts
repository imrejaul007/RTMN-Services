/** Public templates — key by starter type */
export const TEMPLATES: Record<StarterType, (input: RenderInput) => string> = {
  marketplace: render,
  b2b: render,
  company: render,
  hotel: render,
  restaurant: render,
  logistics: render,
  crm: render,
  erp: render,
  pos: render,
  sdk: render,
  library: render,
  other: render
};

/** Main render function. */
export function render(input: RenderInput): string {
  const m = input.manifest;
  const c = input.capability;
  const languageList = m.languages.join(', ');
  // Substitute {{NAME}} globally in the header template (it appears twice).
  const header = TEMPLATE_HEADER
    .replace(/\{\{NAME\}\}/g, m.name)
    .replace('{{SCHEMA_VERSION}}', m.schemaVersion)
    .replace('{{DESCRIPTION_SUFFIX}}', descClause(m))
    .replace('{{REGION}}', m.region ?? 'global')
    .replace('{{LANGUAGES}}', languageList)
    .replace('{{TYPE}}', m.type)
    .replace('{{HOJAI_VERSION}}', m.hojaiVersion);

  const sections = [
    starterPreamble(m.type),
    STACK_SECTION(input),
    AGENT_TABLE(input),
    INTEGRATIONS_SECTION(input),
    c.capabilities.length > 0 ? `\n## Nexha Federation\n\nThis project joins the Global Nexha network with ${c.capabilities.length} capabilities.\n` : '',
    ENTRYPOINTS_SECTION(input),
    SCRIPTS_SECTION(input),
    CONVENTIONS_SECTION(input),
    TEMPLATE_FOOTER
  ];
  return header + sections.join('');
}

/** Pick the right template by starter type. Always falls back to generic. */
export function renderFor(input: RenderInput): string {
  const tmpl = TEMPLATES[input.manifest.type] ?? render;
  return tmpl(input);
}
