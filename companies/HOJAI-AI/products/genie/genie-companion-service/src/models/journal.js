// Generate journal summary
export const generateJournalSummary = (journal) => {
  const summaries = {
    daily: {
      emoji: '📝',
      template: 'Today was a {sentiment} day. The highlight was {highlight}. Main challenge: {challenge}. Grateful for: {gratitude}.'
    },
    reflection: {
      emoji: '💭',
      template: 'Deep thoughts about {topic}. Feeling {mood}. Key insight: {insight}.'
    },
    gratitude: {
      emoji: '🙏',
      template: 'Grateful for {items}. Feeling appreciated and blessed.'
    },
    milestone: {
      emoji: '🏆',
      template: 'Achieved {achievement}! Feeling proud and motivated.'
    },
    emotional: {
      emoji: '❤️',
      template: 'Processing {emotion}. This is valid and important.'
    }
  };

  return summaries[journal.type] || summaries.daily;
};

// Generate AI insights from journal
export const analyzeJournalInsights = (journals) => {
  if (!journals || journals.length < 3) {
    return {
      insights: [],
      patterns: [],
      suggestions: []
    };
  }

  const insights = [];
  const patterns = [];
  const suggestions = [];

  // Analyze mood trends
  const moodScores = journals.map(j => j.moodScore || 5);
  const avgMood = moodScores.reduce((a, b) => a + b, 0) / moodScores.length;

  if (avgMood < 4) {
    insights.push("You've been going through a tough period. Your feelings are valid.");
    suggestions.push("Consider talking to someone you trust about how you've been feeling.");
  } else if (avgMood > 7) {
    insights.push("You're in a great space! This positive energy is wonderful.");
  }

  // Analyze gratitude patterns
  const gratitudeCount = journals.filter(j => j.gratitude && j.gratitude.length > 0).length;
  if (gratitudeCount < journals.length / 3) {
    patterns.push("You could benefit from more gratitude practice.");
    suggestions.push("Try writing 3 things you're grateful for each day.");
  }

  // Analyze sleep patterns
  const sleepEntries = journals.filter(j => j.sleep);
  if (sleepEntries.length > 0) {
    const avgSleep = sleepEntries.reduce((a, j) => a + (j.sleep || 0), 0) / sleepEntries.length;
    if (avgSleep < 6) {
      patterns.push("Your sleep has been below recommended levels.");
      suggestions.push("Aim for 7-9 hours of sleep for better mental health.");
    }
  }

  // Analyze common themes
  const allTags = journals.flatMap(j => j.tags || []);
  const tagCounts = {};
  allTags.forEach(tag => {
    tagCounts[tag] = (tagCounts[tag] || 0) + 1;
  });
  const topTags = Object.entries(tagCounts)
    .sort((a, b) => b[1] - a[1])
    .slice(0, 5)
    .map(([tag]) => tag);

  if (topTags.length > 0) {
    patterns.push(`You often journal about: ${topTags.join(', ')}`);
  }

  return { insights, patterns, suggestions };
};
