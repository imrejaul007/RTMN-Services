/**
 * Journal Model - AI-powered journaling
 */

export class Journal {
  constructor(data) {
    this.id = data.id || uuidv4();
    this.userId = data.userId;
    this.type = data.type || 'daily'; // daily, reflection, gratitude, milestone, emotional
    this.date = data.date || new Date().toISOString().split('T')[0];
    this.title = data.title || '';
    this.content = data.content || '';
    this.mood = data.mood || null; // mood at time of writing
    this.moodScore = data.moodScore || 5;
    this.highlights = data.highlights || []; // best parts of the day
    this.challenges = data.challenges || []; // difficulties faced
    this.gratitude = data.gratitude || []; // things grateful for
    this.lessons = data.lessons || []; // what was learned
    this.goals = data.goals || []; // goals mentioned
    this.people = data.people || []; // people mentioned
    this.activities = data.activities || []; // activities done
    this.energy = data.energy || null;
    this.sleep = data.sleep || null;
    this.sentiment = data.sentiment || 'neutral'; // positive, neutral, negative
    this.aiInsights = data.aiInsights || []; // AI-generated insights
    this.autoGenerated = data.autoGenerated || false;
    this.voiceNote = data.voiceNote || null; // voice note URL
    this.tags = data.tags || [];
    this.createdAt = data.createdAt || new Date().toISOString();
    this.updatedAt = data.updatedAt || new Date().toISOString();
  }

  toJSON() {
    return {
      id: this.id,
      userId: this.userId,
      type: this.type,
      date: this.date,
      title: this.title,
      content: this.content,
      mood: this.mood,
      moodScore: this.moodScore,
      highlights: this.highlights,
      challenges: this.challenges,
      gratitude: this.gratitude,
      lessons: this.lessons,
      goals: this.goals,
      people: this.people,
      activities: this.activities,
      energy: this.energy,
      sleep: this.sleep,
      sentiment: this.sentiment,
      aiInsights: this.aiInsights,
      autoGenerated: this.autoGenerated,
      voiceNote: this.voiceNote,
      tags: this.tags,
      createdAt: this.createdAt,
      updatedAt: this.updatedAt
    };
  }
}

